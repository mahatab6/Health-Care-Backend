import { v2 as cloudinary, UploadApiResponse } from "cloudinary";
import { envVars } from "./env";
import AppError from "../app/errorHelpers/AppError";
import status from "http-status";


cloudinary.config({
    cloud_name: envVars.CLOUDINARY_CLOUD_NAME,
    api_key: envVars.CLOUDINARY_API_KEY,
    api_secret: envVars.CLOUDINARY_API_SECRET
})


export const uploadFileToCloudinary = async (buffer: Buffer, fileName: string): Promise<UploadApiResponse> => {
    if(!buffer || !fileName) {
        throw new AppError(status.BAD_REQUEST, "Buffer or fileName is missing");
    }

    const extension = fileName.split(".").pop()?.toLowerCase();

    const fileNameWithoutExtension = fileName.split(".").slice(0, -1).join(".").toLocaleLowerCase().replace(/\s+/g, "-").replace(/[^a-z0-9\-]/g, "");

    const uniqueName = Math.random().toString(36).substring(2, 8)+"-"+Date.now()+"-"+fileNameWithoutExtension;

    const folder = extension === "pdf" ? "pdfs" : "images";

    return new Promise((resolve, reject) => {
        cloudinary.uploader.upload_stream({
            resource_type: "auto",
            public_id: `healthcare/${folder}/${uniqueName}`,
            folder: `healthcare/${folder}`
        }, (error, result) => {
            if(error) {
                return reject(new AppError(status.INTERNAL_SERVER_ERROR, "Failed to upload file to Cloudinary"));
        }
        resolve(result as UploadApiResponse);
        }).end(buffer);
    })
}



export const deleteFileFromCloudinary = async (url: string) => {
    try {
        const regex = /\/v\d+\/(.+?)(?:\.[a-zA-Z0-9]+)+$/;
        const match = url.match(regex);

        if(match && match[1]) {
            const publicId = match[1];

            await cloudinary.uploader.destroy(publicId, { resource_type: "image" });

            console.log(`File with public ID ${publicId} deleted successfully from Cloudinary.`);
        }
    } catch (error) {
        console.error("Error deleting file from Cloudinary:", error);
        throw new AppError(status.INTERNAL_SERVER_ERROR, "Failed to delete file from Cloudinary");
    }
}


